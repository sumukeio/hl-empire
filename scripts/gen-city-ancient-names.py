def parse_wiki() -> dict[str, list[str]]:
    text = WIKI.read_text(encoding="utf-8")
    by_prov: dict[str, list[str]] = {}
    for line in text.splitlines():
        m = re.search(
            r"\[([^\]]+(?:市|州|盟|地区))\].*\| (?:地级市|自治州|盟|地区) \| ([^|]+) \|",
            line,
        )
        if not m:
            continue
        city, prov = m.group(1), m.group(2).strip()
        by_prov.setdefault(prov, []).append(city)
    return by_prov


def ancient(name: str) -> str:
    if name in ANCIENT:
        return ANCIENT[name]
    # fallback: strip 市/州/盟/地区 for generic
    base = re.sub(r"(市|自治州|地区|盟)$", "", name)
    return base if base != name else "—"


